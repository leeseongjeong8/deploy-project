COMMENT ON COLUMN DEPLOY.SCHEDULE.COMPLETE_DATE IS '완료시간';
COMMENT ON COLUMN DEPLOY.SCHEDULE.COMPLETE_YN IS '완료여부';
COMMENT ON COLUMN DEPLOY.SCHEDULE.END_DATE IS '종료시간';
COMMENT ON COLUMN DEPLOY.SCHEDULE.REGDATE IS '작성일';
COMMENT ON COLUMN DEPLOY.SCHEDULE.SCHEDULE_CONTENT IS '스케쥴내용';
COMMENT ON COLUMN DEPLOY.SCHEDULE.SCHEDULE_NO IS '스케줄번호';
COMMENT ON COLUMN DEPLOY.SCHEDULE.SCHEDULE_TITLE IS '스케쥴제목';
COMMENT ON COLUMN DEPLOY.SCHEDULE.START_DATE IS '시작시간';
COMMENT ON COLUMN DEPLOY.SCHEDULE.UPDATE_DATE IS '변경일';
COMMENT ON COLUMN DEPLOY.SCHEDULE.WRITER IS '작성자';

CREATE UNIQUE INDEX DEPLOY.SCHEDULE_NO_PK ON DEPLOY.SCHEDULE (
	SCHEDULE_NO ASC
)
LOGGING
