CREATE TABLE DEPLOY.MEMBER (
	ID VARCHAR(20) NOT NULL,
	PASSWORD VARCHAR(100) NOT NULL,
	NAME VARCHAR(50) NOT NULL,
	POSITION VARCHAR(50) NOT NULL,
	PHONE VARCHAR(30) NOT NULL,
	EMAIL VARCHAR(100) NOT NULL,
	REGDATE DATE NOT NULL,
	TEAM VARCHAR(100) NOT NULL
)
TABLESPACE USR
PCTFREE 10
INITRANS 2
STORAGE (
	MAXEXTENTS UNLIMITED
)
LOGGING
NOPARALLEL;
COMMENT ON TABLE DEPLOY.MEMBER IS '유저';
COMMENT ON COLUMN DEPLOY.MEMBER.EMAIL IS '이메일';
COMMENT ON COLUMN DEPLOY.MEMBER.ID IS '아이디';
COMMENT ON COLUMN DEPLOY.MEMBER.NAME IS '이름';
COMMENT ON COLUMN DEPLOY.MEMBER.PASSWORD IS '비밀번호';
COMMENT ON COLUMN DEPLOY.MEMBER.PHONE IS '전화번호';
COMMENT ON COLUMN DEPLOY.MEMBER.POSITION IS '직급';
COMMENT ON COLUMN DEPLOY.MEMBER.REGDATE IS '등록일';
COMMENT ON COLUMN DEPLOY.MEMBER.TEAM IS '팀';

CREATE UNIQUE INDEX DEPLOY.ID_PK ON DEPLOY.MEMBER (
	ID ASC
)
LOGGING
TABLESPACE USR
PCTFREE 10
INITRANS 2;

ALTER TABLE DEPLOY.MEMBER ADD CONSTRAINT ID_PK
PRIMARY KEY (
	ID
);
